			throw new \InvalidArgumentException("$destPath is not writable!");
		}
		
		$destPath = $destPath . DIRECTORY_SEPARATOR . $appName;
		$input->setOption('target-dir', $destPath);
		
		
		
		if (!file_exists($destPath))mkdir($destPath);
		
		
